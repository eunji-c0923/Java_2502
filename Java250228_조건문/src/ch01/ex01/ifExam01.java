package ch01.ex01;

public class ifExam01 {

	public static void main(String[] args) {

		
		/*
		 * 조건문
		 * 1.if (참만 있는 경우)
		 * 2.if~ else(조건을 만족하면 앞에 있는게 실행 / 조건을 만족하지 못하면 뒤에 있는게 실행)
		 * 3.if ~ else if ~else
		 * 
		 * swich뮨(if문으로 구성할 수 있다)
		 */
	
	     int kor= 60;
	     
	     
	     if(kor >=70) {
	    	 System.out.println("합격");
	     }
	     
	     if(kor <70) {
	    	 System.out.println("불합격");
	     }
		    
	    System.out.println("종료");
	}
	

}
