package ch01.ex10;

public class OperationExample {

	public static void main(String[] args) {

		
		int a =1;
		
		//a + a + 1;
		a += 1;
		
		System.out.println(a);
		
		a++;
		++a;
		System.out.println(a);
		
		a *= 3;
		System.out.println(a);
		//우측의 피연산자의 값을 변수의 값과 곱한 후에 다시 변수에 저장
		
		
		a -= 10;
		System.out.println(a);
		//우측의 피연산자의 값을 변수의 값에서 뺀 후 다시 변수에 저장
	
		
		
	}

}
